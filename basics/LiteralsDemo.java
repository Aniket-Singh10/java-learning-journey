// Program to demonstrate different types of literals in Java

public class LiteralsDemo {

    public static void main(String[] args) {

        // Integer literal
        byte age = 15;

        // Integer literal
        int rollNo = 56;

        // Character literal
        char ch = 'S';

        // Floating-point literal
        float fl = 26.5f;

        // Double literal
        double dl = 69.09d;

        // Long literal (L is required)
        long l = 6464956000000556656L;

        // Boolean literal (result of a comparison)
        boolean tf = 5 <= 5;

        // String literal
        String str = "Aniket";

        // Printing all values
        System.out.println(age);
        System.out.println(l);
        System.out.println(tf);
        System.out.println(str);
        System.out.println(rollNo);
        System.out.println(ch);
        System.out.println(fl);
        System.out.println(dl);
    }
}

